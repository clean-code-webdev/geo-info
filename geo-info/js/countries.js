function loadInfo(countries) {
  let areas = document.querySelectorAll(".area");

  areas.forEach((area) => {
    area.addEventListener("click", () => {
      let country = area.id;

      // -----

      for (let i = 0; i < countries.length; i++) {
        for (let j = 0; j < countries[i].length; j++) {
          if (countries[i][j].id === country) {
            let info = document.querySelector("#info");

            info.innerHTML = "";

            // -----

            let countryName = document.createElement("h2");
            countryName.id = "country-name";
            countryName.innerText = countries[i][j].id;

            info.appendChild(countryName);

            // -----

            let flag = document.createElement("div");
            flag.id = "flag";
            flag.innerHTML = `<img src="${countries[i][j].flag}" id="country-flag" alt="flag" />`;
            info.appendChild(flag);

            // -----

            let geoLocation = document.createElement("div");
            geoLocation.id = "geo-location";
            geoLocation.innerHTML = `<img src="${countries[i][j].geoLocation}" id="country-geo-location" alt="geo-location" />`;
            info.appendChild(geoLocation);

            // -----

            for (const [key, value] of Object.entries(countries[i][j])) {
              if (
                key !== "id" &&
                key !== "flag" &&
                key !== "geoLocation" &&
                key !== "coords"
              ) {
                console.log(`${key}: ${value}`);

                let p = document.createElement("p");

                p.innerHTML = `<span>${key}</span> <span>:</span> <span>${value}</span>`;

                info.appendChild(p);
              }
            }
          }
        }
      }
    });
  });
}

